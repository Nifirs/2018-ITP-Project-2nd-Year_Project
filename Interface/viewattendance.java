    private void showData() {
        try {
            DBconnection db = new DBconnection();
            Connection con = db.getConnection();

            PreparedStatement ps = con.prepareStatement("SELECT * FROM attendance");
            ResultSet result = ps.executeQuery();

            jTable1.setModel(net.proteanit.sql.DbUtils.resultSetToTableModel(result));

        } catch (SQLException ex) {
            Logger.getLogger(viewattendance.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
