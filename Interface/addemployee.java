    private void submitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitMouseClicked
        if (employeeid.getText().trim().length() == 0) {
            employeeid.setBackground(Color.red);
            basicsalary.setBackground(Color.white);
            otrate.setBackground(Color.white);
        } else if (basicsalary.getText().trim().length() == 0) {
            employeeid.setBackground(Color.white);
            basicsalary.setBackground(Color.red);
            otrate.setBackground(Color.white);
        } else if (otrate.getText().trim().length() == 0) {
            otrate.setBackground(Color.red);
            basicsalary.setBackground(Color.white);
            employeeid.setBackground(Color.white);
            
        } else {
            employeeid.setBackground(Color.white);
            basicsalary.setBackground(Color.white);
            otrate.setBackground(Color.white);
            
            int empid = Integer.valueOf(employeeid.getText());
            double salary = Double.valueOf(basicsalary.getText());
            double otRate = Double.valueOf(otrate.getText());
            try {
//            Class.forName("com.mysql.jdbc.Driver");
//            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/marketing", "root", "");
//            if(con!=null){
                DBconnection db = new DBconnection();
                Connection con = db.getConnection();
                
                String q = "INSERT INTO addemployee(employeeid,basicsalary,otrate) values (?,?,?)";
                PreparedStatement ps = con.prepareStatement(q);
                ps.setInt(1, empid);
                ps.setDouble(2, salary);
                ps.setDouble(3, otRate);
                
                int i = ps.executeUpdate();
                if (i > 0) {
                    JOptionPane.showMessageDialog(null, "Data Updated");
                } else {
                    JOptionPane.showMessageDialog(null, "Data Error");
                }
//            }else{
//                System.out.println("oh");
//            }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        

    }//GEN-LAST:event_submitMouseClicked

