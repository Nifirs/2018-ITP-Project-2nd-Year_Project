    private void jLabel13MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel13MouseClicked
        int companyidd = Integer.valueOf(companyid.getText());
       
        double epfl = Double.valueOf(epf.getText());
        double etfl = Double.valueOf(etf.getText());
        double insurancel = Double.valueOf(insurance.getText());

        try {
          
            DBconnection db = new DBconnection();
            Connection getCOn = db.getConnection();

            String sql = "Update embank1 set epf=?,etf=?,insurance=? WHERE companyid=?";
            PreparedStatement ps = getCOn.prepareStatement(sql);
            ps.setDouble(1, epfl);
            ps.setDouble(2, etfl);
            ps.setDouble(3, insurancel);
            ps.setInt(4, companyidd);

            int i = ps.executeUpdate();

            if (i > 0) {
                JOptionPane.showMessageDialog(null, "Data Updated");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    
    
    }//GEN-LAST:event_jLabel13MouseClicked

