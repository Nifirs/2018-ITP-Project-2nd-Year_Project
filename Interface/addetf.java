    private void submitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitMouseClicked
        if (companyid.getText().trim().length() == 0) {
            epf.setBackground(Color.red);
            etf.setBackground(Color.white);
            insurance.setBackground(Color.white);
            } else if (epf.getText().trim().length() == 0) {
            epf.setBackground(Color.white);
            etf.setBackground(Color.red);
            insurance.setBackground(Color.white);
        } else if (etf.getText().trim().length() == 0) {
            epf.setBackground(Color.white);
            etf.setBackground(Color.red);
            insurance.setBackground(Color.white);
        } else if (insurance.getText().trim().length() == 0) {
            epf.setBackground(Color.red);
            etf.setBackground(Color.white);
            insurance.setBackground(Color.white);

        } 
        else {
            companyid.setBackground(Color.white);
            epf.setBackground(Color.white);
            etf.setBackground(Color.white);
            insurance.setBackground(Color.white);

            int companyidd = Integer.valueOf(companyid.getText());
            double epfl = Double.valueOf(epf.getText());
            double etfl = Double.valueOf(etf.getText());
            double insurancel = Double.valueOf(insurance.getText());
            
            
             try {
                //            Class.forName("com.mysql.jdbc.Driver");
                //            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/marketing", "root", "");
                //            if(con!=null){
                    DBconnection db = new DBconnection();
                    Connection con = db.getConnection();

                    String q = "INSERT INTO embank1(companyid,epf,etf,insurance) values (?,?,?,?)";
                    PreparedStatement ps = con.prepareStatement(q);
                    ps.setInt(1, companyidd);
                    ps.setDouble(2, epfl);
                    ps.setDouble(3, etfl);
                    ps.setDouble(4, insurancel);

                    int i = ps.executeUpdate();
                    if (i > 0) {
                        JOptionPane.showMessageDialog(null, "Data Updated");
                    } else {
                        JOptionPane.showMessageDialog(null, "Data Error");
                    }
                    //            }else{
                    //                System.out.println("oh");
                    //            }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }   

    }//GEN-LAST:event_submitMouseClicked

