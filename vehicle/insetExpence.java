private boolean ValiDate() {
        JTextField textFields[] = new JTextField[]{ex,vidb};
          
        boolean flag = true;

        if (etype.getSelectedIndex() == 0) {
            etype.setBorder(BorderFactory.createLineBorder(Color.red));
            flag = false;

        } else {
            etype.setBorder(UIManager.getLookAndFeel().getDefaults().getBorder("TextField.border"));
            flag = true;

        }
        
         if (date.getDate() == null) {
            date.setBorder(BorderFactory.createLineBorder(Color.red));
            flag = false;

        } else {
            date.setBorder(UIManager.getLookAndFeel().getDefaults().getBorder("TextField.border"));
            flag = true;

        }

        for (JTextField field : textFields) {
            if (field.getText().trim().length() == 0) {
                field.setBackground(Color.red);
                System.out.println("empty");
                flag = false;

            } else {
                field.setBackground(Color.white);
                field.setForeground(Color.black);
                System.out.println("not empty");
                flag = true;
            }
        }

          


        if (flag == true) {
            return true;
        } else {
            return false;
        }

}
