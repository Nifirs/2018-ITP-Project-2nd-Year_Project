private boolean valiDate() {
        JTextField textFields[] = new JTextField[]{qobl, lefletprice};
       
        boolean flag = true;

        if (Leafletsizebox.getSelectedIndex() == 0) {
            Leafletsizebox.setBorder(BorderFactory.createLineBorder(Color.red));
            flag = false;

        } else {
            Leafletsizebox.setBorder(UIManager.getLookAndFeel().getDefaults().getBorder("TextField.border"));
            flag = true;

        }

        for (JTextField field : textFields) {
            if (field.getText().trim().length() == 0) {
                field.setBackground(Color.red);
                System.out.println("empty");
                flag = false;

            } else {
                field.setBackground(Color.white);
                field.setForeground(Color.black);
                System.out.println("not empty");
                flag = true;
            }
        }

       
        if (flag == true) {
            return true;
        } else {
            return false;
        }
}


