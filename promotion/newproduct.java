private boolean valiDate() {
        JTextField textFields[] = new JTextField[]{newpname,qobn,newpprice,sbt};
        JTextArea textAreas[]=new JTextArea[]{newpdis};
       
        boolean flag = true;

        if (npmethod.getSelectedIndex() == 0) {
            npmethod.setBorder(BorderFactory.createLineBorder(Color.red));
            flag = false;

        } else {
            npmethod.setBorder(UIManager.getLookAndFeel().getDefaults().getBorder("TextField.border"));
            flag = true;

        }

        for (JTextField field : textFields) {
            if (field.getText().trim().length() == 0) {
                field.setBackground(Color.red);
                System.out.println("empty");
                flag = false;

            } else {
                field.setBackground(Color.white);
                field.setForeground(Color.black);
                System.out.println("not empty");
                flag = true;
            }
        }

        
        for (JTextArea area : textAreas) {
            if (area.getText().trim().length() == 0) {
                area.setBackground(Color.red);
                System.out.println("empty");
                flag = false;

            } else {
                area.setBackground(Color.white);
                area.setForeground(Color.black);
                System.out.println("not empty");
                flag = true;
            }
        }
        
        
        
        
        
        
        
        
        
        
        
        
        
        
       
        if (flag == true) {
            return true;
        } else {
            return false;
        }
    }


public void viewDetails() {
        try {
           DatabaseConnection getCon = new DatabaseConnection();
                Connection con = getCon.getConnection();

            PreparedStatement ps = con.prepareStatement("SELECT nname,ndis,qtyn,nprice,spn,npm FROM newitem WHERE nid LIKE ?");
           ps.setString(1, "%" + newid.getText() + "%");
            ResultSet result = ps.executeQuery();

            while (result.next()) {
               
             newpname.setText(result.getString("nname"));
                newpdis.setText(result.getString("ndis"));
                qobn.setText(result.getString("qtyn"));
                 newpprice.setText(result.getString("nprice"));
               sbt.setText(result.getString("spn"));
               npmethod.setSelectedItem(result.getString("npm"));
                 
           
                
                
                
              
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        
        }
    }

