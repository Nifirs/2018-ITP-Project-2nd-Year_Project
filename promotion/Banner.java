    private void qobKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_qobKeyReleased
        
        
        
        double sixFive = 1000.00;
        double eightFive = 2000.00;
        double tenFive = 3000.00;
        double twelveFive = 4000.00;

        if (qob.getText().trim().length() != 0) {
            double bannerQuantity = Double.valueOf(qob.getText());
            if (bannersizebox.getSelectedItem().toString().equals("6*5")) {
                bannerprice.setText(String.valueOf(sixFive * bannerQuantity));

            } else if (bannersizebox.getSelectedItem().toString().equals("8*5")) {
                bannerprice.setText(String.valueOf(eightFive * bannerQuantity));
            } else if (bannersizebox.getSelectedItem().toString().equals("10*5")) {
                bannerprice.setText(String.valueOf(tenFive * bannerQuantity));
            } else if (bannersizebox.getSelectedItem().toString().equals("12*5")) {
                bannerprice.setText(String.valueOf(twelveFive * bannerQuantity));
            } else {
                System.out.println("");
            }

        } else {
            bannerprice.setText("");
        }

    }//GEN-LAST:event_qobKeyReleased

