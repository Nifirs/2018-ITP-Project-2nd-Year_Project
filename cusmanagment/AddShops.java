    public void id_auto(){
        
            String sql = "SELECT MAX(RIGHT(shopID, 4)) AS no FROM shops";
            try {
                pst = con.prepareStatement(sql);
            
            rs = pst.executeQuery();
            
            while(rs.next()){
                if(rs.first() == false){
                    ShopIDbox.setText("L0001");
                }
                else{
                    rs.last();
                    int set_id = rs.getInt(1) + 1;
                    String no = String.valueOf(set_id);
                    
                    int id_next = no.length();
                    
                    for(int a = 0 ; a < 4 - id_next ; a++){
                        no = "0" + no;
                    }
                    
                    ShopIDbox.setText("L" + no);
                }
            }
            }catch (SQLException ex) {
                Logger.getLogger(AddShops.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }
    
